#Right Traingle
for i in range(0,5):
    for j in range(0,i+1):
        print('*',end=' ')
    print()

# Print Increasing Number Triangle Pattern

for i in range(1, 6):
    for j in range(1, i + 1):
        print(j, end=' ')
    print()


# Print Repeated Number Triangle Pattern
for i in range(1, 6):
    for j in range(1, i + 1):
        print(i, end=' ')
    print()

